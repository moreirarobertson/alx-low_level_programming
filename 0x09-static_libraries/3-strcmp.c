/**
 * _strcmp - A function use to compare string values
 * @s1: first input string
 * @s2: second input string
 *
 * Return: s1[i] - s2[i] (difference in characters in both strings)
 */
int _strcmp(char *s1, char *s2)
{
	int i;
	i = 0; 
	while (s1[i] != '\0' && s2[i] != '\0') 
		
        return (s1[i] - s2[i]); 	
	i++; 
	return (0); 
}
